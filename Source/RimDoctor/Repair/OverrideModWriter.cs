using System;
using System.Collections.Generic;
using System.IO;
using System.Xml.Linq;
using Verse;

namespace RimDoctor
{
    /// <summary>
    /// Manages the generated "RimDoctor Overrides" local mod — the ONLY place
    /// RimDoctor ever writes file-level fixes. It loads last, so its textures and
    /// PatchOperations win, and we NEVER touch other mods' Workshop files (Steam
    /// would overwrite them anyway).
    ///
    /// Every file written is recorded in a manifest so "Undo last repair" can
    /// remove exactly what a repair run created.
    /// </summary>
    public static class OverrideModWriter
    {
        public const string PackageId = "tyler.rimdoctor.overrides";
        public const string ModName = "RimDoctor Overrides";

        private static readonly List<string> lastRunFiles = new List<string>();
        public static IReadOnlyList<string> LastRunFiles => lastRunFiles;

        public static string Root => RimDoctorPaths.OverrideModFolder;

        /// <summary>Ensure the override mod skeleton exists (About.xml etc). Returns root or null.</summary>
        public static string EnsureSkeleton()
        {
            try
            {
                string root = Root;
                if (string.IsNullOrEmpty(root)) return null;
                Directory.CreateDirectory(Path.Combine(root, "About"));

                string about = Path.Combine(Path.Combine(root, "About"), "About.xml");
                if (!File.Exists(about))
                {
                    var doc = new XDocument(new XElement("ModMetaData",
                        new XElement("name", ModName),
                        new XElement("author", "RimDoctor (generated)"),
                        new XElement("packageId", PackageId),
                        new XElement("description",
                            "Auto-generated by RimDoctor. Contains non-destructive fixes (placeholder/" +
                            "corrected textures and texPath redirects) that load LAST so they win. " +
                            "Safe to delete — RimDoctor will regenerate it. Do not edit by hand."),
                        new XElement("supportedVersions", new XElement("li", "1.6"))));
                    doc.Save(about);
                }
                // loadFolders so 1.6 picks up our content folder.
                string lf = Path.Combine(Path.Combine(root, "About"), "loadFolders.xml");
                if (!File.Exists(lf))
                {
                    var doc = new XDocument(new XElement("loadFolders",
                        new XElement("v1.6", new XElement("li", "/"))));
                    doc.Save(lf);
                }
                return root;
            }
            catch (Exception e)
            {
                RDLog.Exception("Failed to create override mod skeleton", e);
                return null;
            }
        }

        public static void BeginRun() => lastRunFiles.Clear();

        /// <summary>Write bytes to a path under the override mod, recording it for undo.</summary>
        public static bool WriteFile(string relativePath, byte[] bytes)
        {
            try
            {
                string root = EnsureSkeleton();
                if (root == null) return false;
                string full = Path.Combine(root, relativePath.Replace('/', Path.DirectorySeparatorChar));
                Directory.CreateDirectory(Path.GetDirectoryName(full));
                File.WriteAllBytes(full, bytes);
                lastRunFiles.Add(full);
                return true;
            }
            catch (Exception e)
            {
                RDLog.Exception($"Failed to write override file {relativePath}", e);
                return false;
            }
        }

        public static bool CopyFile(string sourceFile, string relativePath)
        {
            try
            {
                string root = EnsureSkeleton();
                if (root == null || !File.Exists(sourceFile)) return false;
                string full = Path.Combine(root, relativePath.Replace('/', Path.DirectorySeparatorChar));
                Directory.CreateDirectory(Path.GetDirectoryName(full));
                File.Copy(sourceFile, full, overwrite: true);
                lastRunFiles.Add(full);
                return true;
            }
            catch (Exception e)
            {
                RDLog.Exception($"Failed to copy into override mod: {sourceFile}", e);
                return false;
            }
        }

        public static void WriteText(string relativePath, string text)
        {
            WriteFile(relativePath, System.Text.Encoding.UTF8.GetBytes(text));
        }

        /// <summary>
        /// Make the just-written override mod discoverable and active so it loads on
        /// the next restart. RimWorld only reads the mod list at startup, so this
        /// must be followed by a restart to take effect. Best-effort: if the mod
        /// can't be activated pre-restart, RimWorld will still discover the folder
        /// on launch and the user can enable it manually.
        /// </summary>
        public static bool RegisterAndActivate()
        {
            try
            {
                // Rescan installed mods so the freshly-written folder is known.
                ModLister.RebuildModList();
                ModsConfig.SetActive(PackageId, true);
                ModsConfig.Save();
                bool active = ModsConfig.IsActive(PackageId);
                RDLog.Msg(active
                    ? $"Enabled '{ModName}' in the active mod list (loads on restart)."
                    : $"Could not auto-enable '{ModName}'; enable it manually after restart.");
                return active;
            }
            catch (Exception e)
            {
                RDLog.Exception("RegisterAndActivate override mod failed", e);
                return false;
            }
        }

        /// <summary>Delete the entire generated override mod (used by full undo).</summary>
        public static bool DeleteAll()
        {
            try
            {
                string root = Root;
                if (string.IsNullOrEmpty(root) || !Directory.Exists(root)) return false;
                Directory.Delete(root, recursive: true);
                lastRunFiles.Clear();
                RDLog.Msg("Deleted generated RimDoctor Overrides mod.");
                return true;
            }
            catch (Exception e)
            {
                RDLog.Exception("Failed to delete override mod", e);
                return false;
            }
        }
    }
}
